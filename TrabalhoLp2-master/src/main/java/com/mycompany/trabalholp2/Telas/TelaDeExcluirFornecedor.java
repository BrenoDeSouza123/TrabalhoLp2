    public void listarFornecedores() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTabela.getModel();
        InterfaceFornDAO dao = new FornecedorDAO();
        List<Fornecedor> fornecedores = new ArrayList<Fornecedor>();
        
        try {
            
            fornecedores = dao.exibirDadosFornecedor();            
            modelo.setRowCount(0);
            
            for (Fornecedor f : fornecedores) {
                
                modelo.addRow(new Object[]{
                    
                    f.getId(),
                    f.getNome(),
                    f.getCNPJ(),
                    f.getTelefone(),
                    f.getEmail()
                        
                });
            }
           
            jRotuloSituacao.setText("Mostrando fornecedores.");
            this.mostrandoFornecedores = true;
            
        } catch (Exception ex) {
            jRotuloResultado.setText("Erro ao listar fornecedores: " + ex.getMessage());
        }
    }
    private void excluirSelecionado() {
        DefaultTableModel modelo = (DefaultTableModel) jTabela.getModel();
        int linha = jTabela.getSelectedRow();
        

        Integer id = (int) modelo.getValueAt(linha, 0);

        int confirm = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o item selecionado?", "Confirmação", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;
        
        if ((id == -1) || (id == null)) {
            
            JOptionPane.showMessageDialog(this, "Selecione o ID de uma linha para excluir.");
            return;
            
            }

        try {
            
            if (mostrandoFornecedores) {
                
                InterfaceFornDAO dao = new FornecedorDAO();
                Fornecedor fornecedor = new Fornecedor();
                fornecedor.setId(id);
                dao.excluir(fornecedor);
                listarFornecedores();
                JOptionPane.showMessageDialog(this, "Fornecedor excluido com sucesso");
                
            }            
            
        } 
        
        catch (Exception ex) {
          
            JOptionPane.showMessageDialog(this, "Falha ao excluir o fornecedor");
            
        }
    }
