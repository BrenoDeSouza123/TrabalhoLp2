    private void excluirSelecionado() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTabela.getModel();
        int linha = jTabela.getSelectedRow();       
        Integer id = (int) modelo.getValueAt(linha, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o item selecionado?", "Confirmação", JOptionPane.YES_NO_OPTION);
        
        if (confirm != JOptionPane.YES_OPTION) return;
        
        if (linha == -1) {
            
            JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir.");
            return;
            
            }

        try {
                        
            if (mostrandoProdutos) {
                
                InterfaceProdDAO dao = new ProdutoDAO();
                Produto produto = new Produto();
                produto.setId(id);
                dao.excluirProduto(produto);
                listarProdutos();
                JOptionPane.showMessageDialog(this, "Produto excluido com sucesso");
                
            }            
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(this, "Falha ao excluir o produto");
            
        }
    }
