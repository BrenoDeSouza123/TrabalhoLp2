    public void listarProdutos() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTabela.getModel();
        InterfaceProdDAO dao = new ProdutoDAO();
        List<Produto> produtos = new ArrayList<Produto>();
        
        try {
            
            produtos = dao.exibirDadosProduto();

            modelo.setRowCount(0);

            for (Produto p : produtos) {
                
                modelo.addRow(new Object[]{
                    
                    p.getId(),
                    p.getNome_prod(),
                    p.getCategoria(),
                    p.getQtde(),
                    p.getFornecedor()
                        
                });
            }

            
            jRotuloSituacao.setText("Mostrando produtos.");
            this.mostrandoProdutos = true;
            
        } catch (Exception ex) {
            
            jRotuloResultado.setText("Erro ao listar produtos\n" + ex.getMessage());
            
        }
        finally {
            
            produtos = null;
            dao = null;
            
        }
    }
