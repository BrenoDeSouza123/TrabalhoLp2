    private void jBotaoEnviarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBotaoEnviarProdActionPerformed
        // TODO add your handling code here:
        
        Integer qtde = Integer.parseInt(jTxtQtdeProduto.getText());
        int linhaSelecionada = jTabelaFornecedor.getSelectedRow();        
        Produto produto = new Produto (jTxtNomeProd.getText(),qtde , jTxtCatProd.getText(), 
        (String) jTabelaFornecedor.getValueAt(linhaSelecionada, 1));
        
        InterfaceProdDAO produtoDAO = new ProdutoDAO();
        
        try {
           
            produtoDAO.cadastrarProduto(produto);
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso");
       
        }catch (SQLException ex) {
           
            JOptionPane.showMessageDialog(this, "Falha ao cadastrar o produto");
        
        } 
        
        finally{
           
            produto = null;    
            produtoDAO = null;
            qtde = null;           
         
        }
    }//GEN-LAST:event_jBotaoEnviarProdActionPerformed

